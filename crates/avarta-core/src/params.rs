/// Layer-3 pigmentation parameters, exposed to the UI separately from the shape
/// table (they drive the reaction–diffusion engine, not the geometry). Same
/// `ParamRange` contract and single-source-of-truth role as [`PARAM_RANGES`];
/// `clamp_in_place` clamps these too. The palette/material (Layer 4) is applied
/// viewer-side and is intentionally not here.
