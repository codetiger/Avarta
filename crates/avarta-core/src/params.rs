//! The user-facing parameter model and its single source of truth.
//!
//! [`ShellParams`] is the full input to [`crate::generate`]; [`PARAM_RANGES`] and
//! [`PIGMENT_RANGES`] are the *only* place each parameter's `min`/`max`/`step`/
//! `default`/`integer` is defined, and [`ShellParams::clamp_in_place`] pins every
//! input to those bounds before any mesh math runs.

use serde::{Deserialize, Serialize};
use std::f32::consts::TAU;

/// Layer-1 coiling parameters (Raup W/D/T + practical extras).
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct ShellParams {
    /// Whorl expansion rate per revolution (> 1). Snails ~1.1–3, Nautilus ~3.
    pub w: f32,
    /// Openness: 0 = whorls touch the axis (tight), → 1 = open coil / wide umbilicus.
    pub d: f32,
    /// Translation rate along the axis. 0 = planispiral (flat), higher = tall spire.
    pub t: f32,
    /// Number of whorls (revolutions).
    pub n: f32,
    /// Aperture aspect ratio (radial semi-axis / axial semi-axis). 1 = circular tube.
    #[serde(default = "default_aspect")]
    pub aspect: f32,

    // --- Layer 2: rib / wave ornamentation (see parameters.md) ---
    /// Axial ribs/waves: number across the whorl, per revolution.
    #[serde(default)]
    pub rib_ax_count: f32,
    /// Axial amplitude as a fraction of tube radius. High = structural wave.
    #[serde(default)]
    pub rib_ax_amp: f32,
    /// Spiral cords: number around the aperture cross-section.
    #[serde(default)]
    pub rib_sp_count: f32,
    /// Spiral amplitude as a fraction of tube radius.
    #[serde(default)]
    pub rib_sp_amp: f32,
    /// Profile: 0 = smooth sine wave, 1 = sharp knife-edge ridge.
    #[serde(default)]
    pub rib_sharp: f32,

    // --- projections: the nodule → spine continuum ---
    /// Projections per whorl (count along the coil). 0 = none.
    #[serde(default)]
    pub proj_count: f32,
    /// Rows of projections around the aperture. 1 = single row (spine-like),
    /// ≥2 = evenly spaced rows (nodulose).
    #[serde(default)]
    pub proj_rows: f32,
    /// Position of the first row around the aperture, radians (0..2π).
    #[serde(default)]
    pub proj_pos: f32,
    /// Projection size (height/length) as a fraction of tube radius.
    #[serde(default)]
    pub proj_size: f32,
    /// Sharpness: 0 = rounded blunt bead (nodule), 1 = narrow needle (spine).
    #[serde(default)]
    pub proj_sharp: f32,

    // --- varices ---
    /// Varices (prominent transverse ridges) per whorl. 0 = none.
    #[serde(default)]
    pub varix_count: f32,
    /// Varix prominence as a fraction of tube radius.
    #[serde(default)]
    pub varix_amp: f32,

    // --- randomness (seeded → reproducible) ---
    /// Random seed (integer-valued). Same seed + params → identical shape.
    #[serde(default)]
    pub seed: f32,
    /// Randomness amount, 0..1. 0 = perfectly uniform (seed has no effect).
    #[serde(default)]
    pub jitter: f32,

    // --- Layer 3: pigmentation (reaction–diffusion at the growing lip) ---
    // The shell's colour pattern is laid down by the mantle edge at the growing
    // aperture, so it is generated by the *same* growth sweep as the geometry: a
    // 1-D reaction–diffusion line runs around the lip (φ) and is stepped once per
    // growth ring (θ). See `pigment_field`.
    /// Pattern regime (integer index into `PIGMENT_RANGES` / `PigRegime`):
    /// 0=solid, 1=spiral bands, 2=axial stripes, 3=oblique lines,
    /// 4=chevrons/tented, 5=spots, 6=reticulated. Selects the RD configuration.
    #[serde(default)]
    pub pig_regime: f32,
    /// Feature scale: small = fine/many features, large = coarse/few. 0..1.
    #[serde(default = "default_pig_scale")]
    pub pig_scale: f32,
    /// Contrast: soft gradient (0) → crisp hard-edged pattern (1). 0..1.
    #[serde(default = "default_pig_contrast")]
    pub pig_contrast: f32,
    /// Feature density along the coil (reaction speed → spacing over growth). 0..1.
    #[serde(default = "default_pig_density")]
    pub pig_density: f32,
    /// Obliqueness: travelling-wave drift around the lip → diagonal lean. 0..1.
    #[serde(default)]
    pub pig_angle: f32,
    /// Irregularity: seeded noise in the pattern (0 = perfectly regular). 0..1.
    #[serde(default)]
    pub pig_irregularity: f32,

    /// Tessellation: segments per revolution along the coil.
    #[serde(default = "default_seg_theta")]
    pub seg_theta: u32,
    /// Tessellation: segments around the aperture cross-section.
    #[serde(default = "default_seg_phi")]
    pub seg_phi: u32,
}

fn default_aspect() -> f32 {
    1.0
}
fn default_pig_scale() -> f32 {
    0.5
}
fn default_pig_contrast() -> f32 {
    0.5
}
fn default_pig_density() -> f32 {
    0.5
}
fn default_seg_theta() -> u32 {
    96
}
fn default_seg_phi() -> u32 {
    48
}

